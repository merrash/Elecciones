public class Ayuntamiento {
    private String localidad;
    private String provincia;
    private String nomAlcalde;
    private String partidoAlcalde;

    
    public void setLocalidad(String loc) {
        localidad = loc;
    }
    public String getLocalidad(){
        return localidad;
    }
    
    public void setProvincia(String prov) {
        provincia = prov;
    }
    public String getProvincia(){
        return provincia;
    }
    
    public void setNomAlcalde(String nomal) {
        nomAlcalde = nomal;
    }
    public String getNomAlcalde(){
        return nomAlcalde;
    }
    
     public void setPartidoAlcalde(String partal) {
        partidoAlcalde = partal;
    }
    public String getPartidoAlcalde(){
        return partidoAlcalde;
    }
    
}
